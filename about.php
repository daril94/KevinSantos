                        los Discos “Paren la Mano Carajo”, “Nuevos Rumbos Melanina”, el single “Tierra de Sol” “Decenio Afro descendiente”.
                        Parte de organización y producción de varios eventos artísticos, como concierto Nuevos Rumbos, Ruta De La Música
                        2013, seis ediciones del “Afro Campal”, Afro Quito, Bembé Juvenil, varias ediciones del evento por el Día Nacional
                        Del Pueblo Afrodescendiente Quito, Mama Marimba.
                        En el 2012 inició su proyecto como solista grabando el primer álbum de sus composiciones llamado “Mis Almas”
                        producido en Guillo Studio; música independiente con los géneros de bomba y marimba haciendo creaciones contemporáneas
                        e interpretaciones de la música tradicional y urbana. Este proyecto tomó más fuerza a mediados del 2015, con una banda
                        establecida teniendo participación en varios eventos en Esmeraldas ,Tulcán , Imbabura y Quito”. Paralelamente trabajando
                        en la docencia instruyendo talleres de marimba y ritmos afro del pacífico en la Escuela de Música de la UDLA ”
                        Universidad De Las Américas” . En Marzo 2017 una de sus experiencias continuas en composiciones “MARIMBEO” logró
                        conseguir un reconocimiento en el concurso nacional de SAYCE “Nuestras Raíces” , igualmente re alizando lugares
                        como Misquilla, Café Democrático, Pobre Diablo, La Estación entre otros escenarios particulares y eventos públicos.

                        Aun con trabajo activo y siempre abierto a nuevas experiencias que permitan visibilizar y difundir este proceso de
